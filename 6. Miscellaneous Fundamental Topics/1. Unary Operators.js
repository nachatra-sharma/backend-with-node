// Unary Operators

// a++ is known as post increment

let a = 10;
b = a++; // First assign then increment
console.log(a, b);

// ++a is known as pre increment

let x = 10;
y = ++x; // First increment then assign
console.log(x, y);

// Unary Plus Operator

let num = "2";
let num2 = +num;
console.log(typeof num);
console.log(typeof num2);

// Unary Minus Operator

let n = -2;
let number = -n;
console.log(number);